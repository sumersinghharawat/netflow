    public function createuserplacementtable($prefix){
        Schema::create("{$prefix}_user_placements", function (Blueprint $table) use ($prefix) {
                    $table->id();
                    $table->foreignId('user_id')->constrained("{$prefix}_users")->onDelete('cascade');
                    $table->foreignId('branch_parent')->nullable()->constrained("{$prefix}_users")->onDelete('cascade');
                    $table->foreignId('left_most')->nullable()->constrained("{$prefix}_users")->onDelete('cascade');
                    $table->foreignId('right_most')->nullable()->constrained("{$prefix}_users")->onDelete('cascade');
                });
    }

    public function updatePlacementTable($sponsor, $user, $position, $prefix){
        $branchParent   =  $sponsor->id;
        $sponsorParent  =  DB::table("{$prefix}_user_placements")->where('user_id', $sponsor->id)
                            ->join("{$prefix}_users" , "{$prefix}_users.id" , "=" , "{$prefix}_user_placements.user_id")
                            ->where("{$prefix}_user_placements.user_id",$sponsor->id)
                            ->first();
        $updateMost     = ($position === 'R')
                            ? ['right_most' => $user->id]
                            : ['left_most' => $user->id];
        if ($sponsorParent) {
            $sponsorParent->branchParent = DB::table("{$prefix}_user_placements")
                ->join("{$prefix}_users", "{$prefix}_users.id", '=', "{$prefix}_user_placements.user_id")
                ->where("{$prefix}_user_placements.user_id", $sponsorParent->branch_parent)
                ->first();
        }
        if(($sponsor->position == $position) && ($sponsorParent->branchParent != null)) {
            $branchParent = $sponsorParent->branchParent->id;
        }
        DB::table("{$prefix}_user_placements")->insert([
            'user_id'       => $user->id,
            'branch_parent' => $branchParent,
        ]);
        DB::table("{$prefix}_user_placements")->where('user_id', $branchParent)->update($updateMost);

    }
