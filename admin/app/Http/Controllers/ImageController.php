    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'active_tree_icon' => 'required|image|mimes:jpeg,png,jpg,gif|max:2048',
            'inactive_tree_icon' => 'required|image|mimes:jpeg,png,jpg,gif|max:2048',
        ]);

        $data = new Image;

        $data->image = $request->image;
        if ($data->image) {
            try {
                $filePath = $this->UserImageUpload($data->image); //Passing $data->image as parameter to our created method
                $data->image = $filePath;
                $data->save();

                return redirect()->back();
            } catch (Exception $e) {
                //Write your error message here
            }
        }
    }

